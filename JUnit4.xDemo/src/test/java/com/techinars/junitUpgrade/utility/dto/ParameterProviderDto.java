package com.techinars.junitUpgrade.utility.dto;

/**
 * @author ayazlakdawala
 * @version May 21, 2014
 */
public class ParameterProviderDto {

	private Integer divisor;
	private Integer dividend;

	/**
	 * @return the divisor
	 */
	public Integer getDivisor() {
		return divisor;
	}

	/**
	 * @param divisor
	 *            the divisor to set
	 */
	public void setDivisor(Integer divisor) {
		this.divisor = divisor;
	}

	/**
	 * @return the dividend
	 */
	public Integer getDividend() {
		return dividend;
	}

	/**
	 * @param dividend
	 *            the dividend to set
	 */
	public void setDividend(Integer dividend) {
		this.dividend = dividend;
	}

}
